#include "config.h"
#include "app.h"


namespace Core {
    //------------------------------------------------------------------------------
    /**
*/
    App::App()
        : isOpen(false),
          exitCode(0) {
        // empty
    }

    //------------------------------------------------------------------------------
    /**
*/
    App::~App() {
        // empty
    }


    //------------------------------------------------------------------------------
    /**
*/
    bool App::Open() {
        assert(!this->isOpen);
        this->isOpen = true;
        return true;
    }

    //------------------------------------------------------------------------------
    /**
*/
    void App::Run() {
        // override in subclass
    }

    //------------------------------------------------------------------------------
    /**
*/
    void App::Close() {
        assert(this->isOpen);
        this->isOpen = false;
    }

    //------------------------------------------------------------------------------
    /**
*/
    void App::Exit() {}

    //------------------------------------------------------------------------------
    /**
*/
    void App::SetExitCode(int exitCode) { this->exitCode = exitCode; }

    //------------------------------------------------------------------------------
    /**
*/
    int App::ExitCode() const { return this->exitCode; }
} // namespace Application